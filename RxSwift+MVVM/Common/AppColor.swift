import UIKit

struct AppColor {
}

extension AppColor {
    enum TextColor: String {
        case main = "FFFFFF"
        case muted = "637394"
    }
}

extension AppColor {
    enum BackGround: String {
        case main = "1A2232"
    }
}

extension AppColor {
    enum Surface: String {
        case main = "1A2232"
        case fade = "1F293D"
        case deep = "253554"
        case mainPressed = "202B40"
    }
}

extension AppColor {
    enum Primary: String {
        case main = "FF8036"
        case glass = "1F293D"
    }
}
